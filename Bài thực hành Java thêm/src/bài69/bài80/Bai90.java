    public Double ktraso(Double a)
    {
        boolean check=false;
        double n=0;
        while(!check)
        {
            try {
               n=a;
               check=true;
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Phải nhập số");
                txtdiem1.setText("");
                txtdiem2.setText("");
                txtdiem1.requestFocus();
            }
        }
        return n;
    }
     public boolean trungnhanvien()
    {
        for(int i=0;i<jTable1.getRowCount();i++)
            if(txtmssv.getText().equals(jTable1.getValueAt(i, 0)))
                return true;
            return false;
    }
