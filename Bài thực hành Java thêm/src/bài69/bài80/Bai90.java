    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model=(DefaultTableModel) jTable1.getModel();
         try {
            Double.parseDouble(txtdiem1.getText());
            Double.parseDouble(txtdiem2.getText());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Điểm phải nhập số");
            txtdiem1.setText("");
                txtdiem2.setText("");
                txtdiem1.requestFocus();
        }
        if(txtmssv.getText().isEmpty()||txthoten.getText().isEmpty()||txtdiem1.getText().isEmpty()||txtdiem2.getText().isEmpty())
            JOptionPane.showMessageDialog(this, "Dữ liệu chưa đầy đủ");
        else if(trungnhanvien())
            JOptionPane.showMessageDialog(this, "Mã sinh viên bị trùng");
        else{
        Object[] data=new Object[7];
        data[0]=txtmssv.getText();
        data[1]=txthoten.getText();
        data[2]=jComboBox1.getSelectedItem().toString();
        data[3]=txtdiem1.getText();
        data[4]=txtdiem2.getText();
        data[5]=txtdiemtb.getText();
        data[6]=txtkq.getText();
        model.addRow(data);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

